DROP INDEX PK_USER_INFO;

/* USER */
DROP TABLE USER_INFO 
	CASCADE CONSTRAINTS;

/* USER */
CREATE TABLE USER_INFO (
	M_SEQ NUMBER NOT NULL, /* 관리용 번호 */
	ID VARCHAR2(20) NOT NULL, /* 아이디 */
	NICKNAME VARCHAR2(20) NOT NULL, /* 닉네임 */
	PASSWORD VARCHAR2(20) NOT NULL, /* 비밀번호 */
	NAME VARCHAR2(10), /* 이름 */
	BIRTH DATE, /* 생년월일 */
	EMAIL VARCHAR2(100), /* 이메일 */
	BACKUP_QUESTION VARCHAR2(100), /* 복구용 질문 */
	BACKUP_ANSWER VARCHAR2(100), /* 복구용 질문 답변 */
	POINT NUMBER DEFAULT 0, /* 포인트(등급) */
	JOIN_DATE DATE DEFAULT SYSDATE
, /* 가입일 */
	STATUS NUMBER(1) DEFAULT 1 /* 상태(일반/비활성화/관리자) */
);

COMMENT ON TABLE USER_INFO IS 'USER';

COMMENT ON COLUMN USER_INFO.M_SEQ IS '관리용 번호';

COMMENT ON COLUMN USER_INFO.ID IS '아이디';

COMMENT ON COLUMN USER_INFO.NICKNAME IS '닉네임';

COMMENT ON COLUMN USER_INFO.PASSWORD IS '비밀번호';

COMMENT ON COLUMN USER_INFO.NAME IS '이름';

COMMENT ON COLUMN USER_INFO.BIRTH IS '생년월일';

COMMENT ON COLUMN USER_INFO.EMAIL IS '이메일';

COMMENT ON COLUMN USER_INFO.BACKUP_QUESTION IS '복구용 질문';

COMMENT ON COLUMN USER_INFO.BACKUP_ANSWER IS '복구용 질문 답변';

COMMENT ON COLUMN USER_INFO.POINT IS '포인트(등급)';

COMMENT ON COLUMN USER_INFO.JOIN_DATE IS '가입일';

COMMENT ON COLUMN USER_INFO.STATUS IS '상태(일반/비활성화/관리자)';

CREATE UNIQUE INDEX PK_USER_INFO
	ON USER_INFO (
		M_SEQ ASC
	);

ALTER TABLE USER_INFO
	ADD
		CONSTRAINT PK_USER_INFO
		PRIMARY KEY (
			M_SEQ
		);